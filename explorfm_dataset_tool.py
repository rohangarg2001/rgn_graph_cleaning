# ── I/O helpers ───────────────────────────────────────────────────────────────

def load_yaml(path: Path) -> dict:
    if path.exists():
        d = yaml.safe_load(path.read_text()) or {}
        return {"included": set(d.get("included") or []),
                "excluded": set(d.get("excluded") or [])}
    return {"included": set(), "excluded": set()}


def save_yaml(state: dict, path: Path):
    header = (
        "# ExplORFM manual cleaning results - generated by explorfm_dataset_tool.py\n"
        "# Entry format:  param_subdir/frame_index\n"
        "# Source graphs : rgb_to_graph_explorfm_clean/<mission>_explorfm_clean/<param>/graph_XXXXXX.json\n"
        "# Cleaned graphs: rgb_to_graph_explorfm_manually_cleaned/<mission>_manually_cleaned/<param>/graph_XXXXXX.json\n\n"
    )
    data = {"included": sorted(state["included"]),
            "excluded": sorted(state["excluded"])}
    path.parent.mkdir(parents=True, exist_ok=True)
    with open(path, "w") as f:
        f.write(header)
        yaml.dump(data, f, default_flow_style=False, sort_keys=False)


def load_json(path: Path) -> dict:
    return json.loads(path.read_text()) if path.exists() else {}


def save_json(path: Path, data: dict):
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(data, indent=2))


# ── Dataset helpers ───────────────────────────────────────────────────────────

def get_missions(input_root: Path, names: list | None, param: str | None) -> list[Path]:
    """Return param subdirs that contain graph_*.json files.

    Structure: input_root/{mission}_explorfm_clean/{mission}__fst{val}.../graph_*.json
    Each returned Path is a param subdir; its .name is used as the mission key.
    """
    result = []
    for clean_dir in sorted(d for d in input_root.iterdir()
                            if d.is_dir() and d.name.endswith("_explorfm_clean")):
        mission_name = clean_dir.name[: -len("_explorfm_clean")]
        if names and not any(pat in mission_name for pat in names):
            continue
        for param_dir in sorted(d for d in clean_dir.iterdir() if d.is_dir()):
            if param and param not in param_dir.name:
                continue
            if any(param_dir.glob("graph_[0-9]*.json")):
                result.append(param_dir)
    if not result:
        print(f"[warn] no missions/params found under {input_root}"
              + (f" matching {names}" if names else "")
              + (f" with param={param}" if param else ""))
    return result


def get_frames(mp: Path) -> list[str]:
    return sorted(p.stem.split("_", 1)[1]
                  for p in mp.glob("graph_[0-9]*.json"))


def fkey(mname: str, fidx: str) -> str:
    return f"{mname}/{fidx}"


# ── Geometry helpers ──────────────────────────────────────────────────────────

def fit_scale(shape) -> float:
    h, w = shape[:2]
    return min(MAX_W / w, MAX_H / h, 1.0)


def to_disp(px, py, s) -> tuple[int, int]:
    return int(px * s), int(py * s)


def norm_rect(a, b) -> tuple:
    return (min(a[0], b[0]), min(a[1], b[1]),
            max(a[0], b[0]), max(a[1], b[1]))


def pt_in_rects(x, y, rects) -> bool:
    return any(x1 <= x <= x2 and y1 <= y <= y2 for x1, y1, x2, y2 in rects)


def trav_colour(score: float) -> tuple[int, int, int]:
    """Map trav_score in [0, 1] → BGR red(0) → yellow(0.5) → green(1)."""
    s = max(0.0, min(1.0, float(score)))
    if s < 0.5:
        # red(0,0,255) → yellow(0,255,255)
        return (0, int(255 * (s / 0.5)), 255)
    # yellow(0,255,255) → green(0,255,0)
    return (0, 255, int(255 * (1 - (s - 0.5) / 0.5)))


# ── Main tool ─────────────────────────────────────────────────────────────────

class ExplorfmTool:

    def __init__(self, all_frames, state, yaml_path,
                 input_root, rgb_root, output_root,
                 auto_thresh, skip_labeled, no_confirm=False):
        self.all_frames  = all_frames
        self.total       = len(all_frames)
        self.state       = state
        self.yaml_path   = yaml_path
        self.input_root  = input_root
        self.rgb_root    = rgb_root
        self.output_root = output_root
        self.auto_thresh = auto_thresh
        self.no_confirm  = no_confirm

        # Per-frame node-edit state
        self.rects:  list  = []
        self.drawing       = False
        self.pt0           = None
        self.pt1           = None
        self.show_removed  = True   # toggled with `h`

        # Current-frame cache (populated by _load)
        self._orig:  np.ndarray | None = None
        self._scale: float             = 1.0
        self._graph: dict              = {}
        self._gpath: Path | None       = None   # output path (write target)
        self._is_fresh: bool           = False  # True if _gpath did not yet exist

        self.idx = 0
        if skip_labeled:
            self.idx = self._find_unlabeled(start=0) or 0

    # ── Path mapping ──────────────────────────────────────────────────────────

    def _src_path(self, mp: Path, fidx: str) -> Path:
        return mp / f"graph_{fidx}.json"

    def _out_path(self, mname: str, fidx: str) -> Path:
        # mname = e.g. "2024-11-02-17-10-25__fst0.35_mnd0.35_gmd0.35"
        mission = mname.split("__")[0]
        return (self.output_root
                / f"{mission}_explorfm_manually_cleaned"
                / mname
                / f"graph_{fidx}.json")

    def _rgb_path(self, mname: str, fidx: str) -> Path:
        mission = mname.split("__")[0]
        p = self.rgb_root / mission / f"{fidx}.jpeg"
        if not p.exists():
            p = self.rgb_root / mission / f"rgb_{fidx}.png"
        return p

    # ── Pre-seed from trav_score ──────────────────────────────────────────────

    def _seed_from_trav(self, g: dict) -> dict:
        """Move all nodes with trav_score < self.auto_thresh from `nodes` to
        `removed_nodes`, dropping any edges that touched them. Idempotent: a
        node is only ever in one bucket."""
        kept = []
        removed = list(g.get("removed_nodes", []))
        rem_ids = {n["id"] for n in removed}
        for n in g.get("nodes", []):
            score = n.get("trav_score", 1.0)
            if score is None:
                score = 1.0
            if score < self.auto_thresh and n["id"] not in rem_ids:
                removed.append(n)
                rem_ids.add(n["id"])
            else:
                kept.append(n)
        keep_e, rem_e = [], list(g.get("removed_edges", []))
        for e in g.get("edges", []):
            if e["node_id_0"] in rem_ids or e["node_id_1"] in rem_ids:
                rem_e.append(e)
            else:
                keep_e.append(e)
        g["nodes"]          = kept
        g["removed_nodes"]  = removed
        g["edges"]          = keep_e
        g["removed_edges"]  = rem_e
        g["explorfm_auto_thresh"] = self.auto_thresh
        return g

    # ── Unlabeled navigation ──────────────────────────────────────────────────

    def _is_unlabeled(self, i: int) -> bool:
        _, mn, f = self.all_frames[i]
        k = fkey(mn, f)
        return k not in self.state["included"] and k not in self.state["excluded"]

    def _find_unlabeled(self, start: int) -> int | None:
        for i in range(start, self.total):
            if self._is_unlabeled(i):
                return i
        return None

    # ── Mouse callback ────────────────────────────────────────────────────────

    def mouse_cb(self, event, x, y, _flags, _p):
        if event == cv2.EVENT_LBUTTONDOWN:
            self.drawing = True
            self.pt0 = self.pt1 = (x, y)
        elif event == cv2.EVENT_MOUSEMOVE and self.drawing:
            self.pt1 = (x, y)
        elif event == cv2.EVENT_LBUTTONUP and self.drawing:
            self.drawing = False
            self.pt1 = (x, y)
            r = norm_rect(self.pt0, self.pt1)
            if (r[2] - r[0]) > 4 and (r[3] - r[1]) > 4:
                self.rects.append(r)
                if self.no_confirm:
                    self.apply_removal()
            self.pt0 = self.pt1 = None

    # ── Graph edit operations ─────────────────────────────────────────────────

    def _ids_in_rects(self, nodes: list) -> set:
        if not self.rects:
            return set()
        s = self._scale
        return {n["id"] for n in nodes
                if pt_in_rects(*to_disp(*n["pixel"], s), self.rects)}

    def _selected_active_ids(self) -> set:
        if not self._graph:
            return set()
        return self._ids_in_rects(self._graph.get("nodes", []))

    def _selected_removed_ids(self) -> set:
        if not self._graph:
            return set()
        return self._ids_in_rects(self._graph.get("removed_nodes", []))

    def apply_removal(self):
        sel = self._selected_active_ids()
        if not sel:
            return
        g = self._graph
        g.setdefault("removed_nodes", [])
        g.setdefault("removed_edges", [])

        keep_n = [n for n in g["nodes"] if n["id"] not in sel]
        rem_n  = [n for n in g["nodes"] if n["id"] in sel]
        keep_e = [e for e in g["edges"]
                  if e["node_id_0"] not in sel and e["node_id_1"] not in sel]
        rem_e  = [e for e in g["edges"]
                  if e["node_id_0"] in sel or e["node_id_1"] in sel]

        g["nodes"] = keep_n
        g["removed_nodes"].extend(rem_n)
        g["edges"] = keep_e
        g["removed_edges"].extend(rem_e)
        save_json(self._gpath, g)
        self._is_fresh = False
        self.rects = []
        print(f"  [{self._gpath.name}] -{len(rem_n)} nodes  -{len(rem_e)} edges  "
              f"(active: {len(keep_n)}  total removed: {len(g['removed_nodes'])})")

    def apply_restore(self):
        """Move highlighted nodes from removed_nodes → nodes, and restore any
        edge in removed_edges whose endpoints are both active afterwards."""
        sel = self._selected_removed_ids()
        if not sel:
            return
        g = self._graph
        g.setdefault("removed_nodes", [])
        g.setdefault("removed_edges", [])

        restore_n = [n for n in g["removed_nodes"] if n["id"] in sel]
        keep_rem  = [n for n in g["removed_nodes"] if n["id"] not in sel]
        g["nodes"].extend(restore_n)
        g["removed_nodes"] = keep_rem

        active_ids = {n["id"] for n in g["nodes"]}
        restore_e = [e for e in g["removed_edges"]
                     if e["node_id_0"] in active_ids
                     and e["node_id_1"] in active_ids]
        keep_rem_e = [e for e in g["removed_edges"]
                      if not (e["node_id_0"] in active_ids
                              and e["node_id_1"] in active_ids)]
        g["edges"].extend(restore_e)
        g["removed_edges"] = keep_rem_e

        save_json(self._gpath, g)
        self._is_fresh = False
        self.rects = []
        print(f"  [{self._gpath.name}] +{len(restore_n)} nodes  "
              f"+{len(restore_e)} edges restored  "
              f"(active: {len(g['nodes'])}  remaining removed: {len(keep_rem)})")

    def undo_edit(self):
        """Restore everything in `removed_nodes`/`removed_edges`, then re-apply
        the trav-score pre-seed so the frame returns to its as-loaded state."""
        g = self._graph
        rn = g.pop("removed_nodes", []) or []
        re = g.pop("removed_edges", []) or []
        g.setdefault("nodes", []).extend(rn)
        g.setdefault("edges", []).extend(re)
        g = self._seed_from_trav(g)
        self._graph = g
        save_json(self._gpath, g)
        print(f"  [{self._gpath.name}] reset to trav_score≥{self.auto_thresh:.2f} "
              f"(active: {len(g['nodes'])}  removed: {len(g['removed_nodes'])})")

    # ── Frame label ops ───────────────────────────────────────────────────────

    def _label(self, bucket: str):
        _, mn, f = self.all_frames[self.idx]
        k = fkey(mn, f)
        other = "excluded" if bucket == "included" else "included"
        self.state[other].discard(k)
        self.state[bucket].discard(k)
        self.state[bucket].add(k)
        save_yaml(self.state, self.yaml_path)
        self._advance()

    def _advance(self):
        if self.idx < self.total - 1:
            self.idx += 1
            self._load()

    # ── Rendering ─────────────────────────────────────────────────────────────

    def _draw_node(self, img, n, s, colour, marker_size=6, thickness=1):
        """Star for frontier nodes, filled circle for everything else."""
        dp = to_disp(*n.get("pixel", (0, 0)), s)
        if n.get("type") == "frontier":
            cv2.drawMarker(img, dp, (0, 0, 0), cv2.MARKER_STAR,
                           marker_size + 2, thickness + 1, cv2.LINE_AA)
            cv2.drawMarker(img, dp, colour, cv2.MARKER_STAR,
                           marker_size, thickness, cv2.LINE_AA)
        else:
            cv2.circle(img, dp, 3, (0, 0, 0), -1, cv2.LINE_AA)
            cv2.circle(img, dp, 2, colour,    -1, cv2.LINE_AA)

    def _render(self) -> np.ndarray:
        s   = self._scale
        img = self._orig.copy()
        if s < 1.0:
            h, w = self._orig.shape[:2]
            img = cv2.resize(img, (int(w * s), int(h * s)),
                             interpolation=cv2.INTER_AREA)

        g       = self._graph
        sel_rem = self._selected_active_ids()    # active   → will be removed
        sel_add = self._selected_removed_ids()   # removed  → will be restored

        # Removed nodes — drawn first so active nodes stack on top.
        # Hidden when `show_removed` is False (toggle with `h`).
        if self.show_removed:
            for n in g.get("removed_nodes", []):
                colour = C_RESTORE if n["id"] in sel_add else C_REMOVED
                self._draw_node(img, n, s, colour)

        # Active nodes — colour-tinted by trav_score; red overlay if pending remove.
        for n in g.get("nodes", []):
            score  = n.get("trav_score", 1.0)
            colour = C_SELECTED if n["id"] in sel_rem else trav_colour(score)
            self._draw_node(img, n, s, colour)

        # Confirmed rectangles
        for (x1, y1, x2, y2) in self.rects:
            ov = img.copy()
            cv2.rectangle(ov,  (x1, y1), (x2, y2), C_RECT, -1)
            cv2.addWeighted(ov, 0.13, img, 0.87, 0, img)
            cv2.rectangle(img, (x1, y1), (x2, y2), C_RECT, 2)

        # Live drag rectangle
        if self.drawing and self.pt0 and self.pt1:
            cv2.rectangle(img, self.pt0, self.pt1, (180, 220, 255), 1)

        self._draw_hud(img, sel_rem, sel_add)
        return img

    def _draw_hud(self, img, sel_rem: set, sel_add: set):
        h, w  = img.shape[:2]
        fn    = cv2.FONT_HERSHEY_SIMPLEX
        g     = self._graph
        _, mn, fidx = self.all_frames[self.idx]

        # Bars
        for (y0, y1) in [(0, 132), (h - 42, h)]:
            ov = img.copy()
            cv2.rectangle(ov, (0, y0), (w, y1), (0, 0, 0), -1)
            cv2.addWeighted(ov, 0.55, img, 0.45, 0, img)

        # Status badge
        k = fkey(mn, fidx)
        if   k in self.state["included"]: s_txt, s_col = "KEPT",     (55, 210, 55)
        elif k in self.state["excluded"]: s_txt, s_col = "REJECTED", (55,  55, 210)
        else:                              s_txt, s_col = "UNLABELED",(160, 160, 55)
        (tw, _), _ = cv2.getTextSize(s_txt, fn, 0.70, 2)
        cv2.putText(img, s_txt, (w - tw - 14, 32), fn, 0.70, s_col, 2, cv2.LINE_AA)

        # Mission / param lines
        parts = mn.split("__", 1)
        cv2.putText(img, f"Mission: {parts[0]}",
                    (10, 26), fn, 0.50, (210, 210, 210), 1, cv2.LINE_AA)
        if len(parts) > 1:
            cv2.putText(img, f"Params:  {parts[1]}",
                        (10, 44), fn, 0.44, (180, 180, 180), 1, cv2.LINE_AA)

        n_inc = len(self.state["included"])
        n_exc = len(self.state["excluded"])
        cv2.putText(img,
                    f"Frame: {fidx}  [{self.idx+1}/{self.total}]   "
                    f"kept: {n_inc}  rejected: {n_exc}  "
                    f"unlabeled: {self.total - n_inc - n_exc}",
                    (10, 62), fn, 0.49, (200, 200, 200), 1, cv2.LINE_AA)

        na   = len(g.get("nodes", []))
        nr   = len(g.get("removed_nodes", []))
        ns_r = len(sel_rem)
        ns_a = len(sel_add)
        rem_vis = "shown" if self.show_removed else "HIDDEN"
        stats = (f"Nodes active: {na}   removed: {nr} ({rem_vis})   "
                 f"trav>={self.auto_thresh:.2f} pre-seed")
        if ns_r: stats += f"   pending remove: {ns_r}"
        if ns_a: stats += f"   pending restore: {ns_a}"
        col = (60, 110, 230) if ns_r else \
              ((230, 200, 40) if ns_a else (160, 215, 160))
        cv2.putText(img, stats, (10, 90), fn, 0.49, col, 1, cv2.LINE_AA)

        # Trav-score legend strip (top-right, under status)
        bar_x, bar_y, bar_w, bar_h = w - 220, 74, 200, 10
        for i in range(bar_w):
            t = i / max(bar_w - 1, 1)
            c = trav_colour(t)
            cv2.line(img, (bar_x + i, bar_y), (bar_x + i, bar_y + bar_h),
                     c, 1, cv2.LINE_AA)
        cv2.rectangle(img, (bar_x, bar_y), (bar_x + bar_w, bar_y + bar_h),
                      (220, 220, 220), 1)
        cv2.putText(img, f"trav 0", (bar_x - 4, bar_y + bar_h + 14),
                    fn, 0.36, (200, 200, 200), 1, cv2.LINE_AA)
        cv2.putText(img, "1", (bar_x + bar_w - 4, bar_y + bar_h + 14),
                    fn, 0.36, (200, 200, 200), 1, cv2.LINE_AA)
        # Threshold tick on the legend
        tx = bar_x + int(bar_w * self.auto_thresh)
        cv2.line(img, (tx, bar_y - 3), (tx, bar_y + bar_h + 3),
                 (255, 255, 255), 1, cv2.LINE_AA)

        # Controls footer
        ctrl = ("drag=select  r=remove  a=add-back  c=clear  u=reset-to-thresh  "
                "h=show/hide-removed  "
                "1=keep  2=reject  s=skip  j=next-unlabeled  "
                "n/b=nav  q=quit")
        cv2.putText(img, ctrl, (10, h - 13), fn, 0.42,
                    (165, 165, 165), 1, cv2.LINE_AA)

    # ── Main event loop ───────────────────────────────────────────────────────

    def run(self):
        if not self.all_frames:
            print("No frames found.")
            return

        cv2.namedWindow(WINDOW, cv2.WINDOW_NORMAL)
        cv2.resizeWindow(WINDOW, MAX_W, MAX_H)
        cv2.setMouseCallback(WINDOW, self.mouse_cb)
        self._load()

        while True:
            cv2.imshow(WINDOW, self._render())
            key = cv2.waitKey(30) & 0xFF

            if   key in (ord('n'), 83):     # n / right
                if self.idx < self.total - 1:
                    self.idx += 1; self.rects = []; self._load()
            elif key in (ord('b'), 81):     # b / left
                if self.idx > 0:
                    self.idx -= 1; self.rects = []; self._load()
            elif key == ord('j'):
                nxt = self._find_unlabeled(self.idx + 1)
                if nxt is not None:
                    self.idx = nxt; self.rects = []; self._load()
            elif key in (ord('q'), 27):
                break
            elif key == ord('1'): self._label("included")
            elif key == ord('2'): self._label("excluded")
            elif key == ord('s'): self._advance()
            elif key == ord('r'): self.apply_removal()
            elif key == ord('a'): self.apply_restore()
            elif key == ord('c'): self.rects = []
            elif key == ord('u'): self.undo_edit()
            elif key == ord('h'):
                self.show_removed = not self.show_removed
                print(f"  removed nodes: "
                      f"{'shown' if self.show_removed else 'hidden'}")

        cv2.destroyAllWindows()
        ni = len(self.state["included"])
        ne = len(self.state["excluded"])
        print(f"\n  Quit — kept: {ni}  rejected: {ne}  "
              f"unlabeled: {self.total - ni - ne}")

    # ── Frame loader ──────────────────────────────────────────────────────────

    def _load(self):
        mp, mn, fidx = self.all_frames[self.idx]
        rgb_p = self._rgb_path(mn, fidx)
        out_p = self._out_path(mn, fidx)
        src_p = self._src_path(mp, fidx)

        img = cv2.imread(str(rgb_p))
        if img is None:
            print(f"[warn] cannot load {rgb_p}")
            img = np.zeros((720, 1280, 3), dtype=np.uint8)

        if out_p.exists():
            g = load_json(out_p)
            self._is_fresh = False
        else:
            g = load_json(src_p)
            g = self._seed_from_trav(g)
            save_json(out_p, g)
            self._is_fresh = True

        self._orig  = img
        self._scale = fit_scale(img.shape)
        self._graph = g
        self._gpath = out_p

        self.rects = []
        self.drawing = False
        self.pt0 = self.pt1 = None


# ── Bulk revert ───────────────────────────────────────────────────────────────

def revert_all(missions: list[Path], output_root: Path):
    """Delete every output JSON for the given param subdirs; the next visit will
    reload from the input root and re-apply the trav-score pre-seed."""
    print(f"\n  Reverting output JSONs in {len(missions)} param subdir(s)…")
    total = removed = 0
    for mp in missions:
        mission = mp.name.split("__")[0]
        out_dir = output_root / f"{mission}_explorfm_manually_cleaned" / mp.name
        if not out_dir.is_dir():
            continue
        for gp in sorted(out_dir.glob("graph_[0-9]*.json")):
            total += 1
            gp.unlink()
            removed += 1
    print(f"  Scanned {total} files; deleted {removed}.\n")


# ── Entry point ───────────────────────────────────────────────────────────────

def main():
    ap = argparse.ArgumentParser(
        description="Manual cleaning tool for ExplORFM auto-cleaned graph JSONs.",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=__doc__,
    )
    ap.add_argument("--input-root",  type=Path, default=DEFAULT_INPUT_ROOT,
                    metavar="DIR",
                    help=f"ExplORFM auto-cleaned graphs root. "
                         f"Default: {DEFAULT_INPUT_ROOT}")
    ap.add_argument("--rgb-root",    type=Path, default=DEFAULT_RGB_ROOT,
                    metavar="DIR",
                    help=f"RGB frames root (rgb_XXXXXX.png per mission). "
                         f"Default: {DEFAULT_RGB_ROOT}")
    ap.add_argument("--output-root", type=Path, default=DEFAULT_OUTPUT_ROOT,
                    metavar="DIR",
                    help=f"Where manually-cleaned graphs are written. "
                         f"Default: {DEFAULT_OUTPUT_ROOT}")
    ap.add_argument("--missions",    nargs="+", metavar="NAME",
                    help="Mission timestamp patterns to include (partial match). "
                         "Default: all.")
    ap.add_argument("--param",       type=str,  default=None, metavar="VAL",
                    help="Filter to a specific parameter value, e.g. 0.35. "
                         "Default: all parameter subdirs.")
    ap.add_argument("--yaml",        type=Path, default=DEFAULT_YAML,
                    metavar="PATH",
                    help=f"YAML file path for kept/rejected frame labels. "
                         f"Default: {DEFAULT_YAML}")
    ap.add_argument("--auto-thresh", type=float, default=DEFAULT_THRESH,
                    metavar="T",
                    help="On first visit to a frame, pre-seed removed_nodes "
                         f"with anything trav_score < T. Default: {DEFAULT_THRESH}.")
    ap.add_argument("--fresh",       action="store_true",
                    help="Wipe YAML and clear --output-root before starting.")
    ap.add_argument("--skip-labeled", action="store_true",
                    help="Auto-jump to the first unlabeled frame on startup.")
    ap.add_argument("--revert-all",  action="store_true",
                    help="Delete every output JSON for the selected missions, "
                         "then exit. Honours --missions.")
    ap.add_argument("--no-confirm",  action="store_true",
                    help="Apply node removal immediately on mouse-up.")
    args = ap.parse_args()

    if not args.input_root.is_dir():
        print(f"[error] --input-root not a directory: {args.input_root}")
        sys.exit(1)
    if not args.rgb_root.is_dir():
        print(f"[error] --rgb-root not a directory: {args.rgb_root}")
        sys.exit(1)
    if not (0.0 <= args.auto_thresh <= 1.0):
        print(f"[error] --auto-thresh must be in [0, 1]; got {args.auto_thresh}")
        sys.exit(1)

    missions = get_missions(args.input_root, args.missions, args.param)
    if not missions:
        print("[error] No param subdirs found.")
        sys.exit(1)

    if args.revert_all:
        revert_all(missions, args.output_root)
        return

    if args.fresh:
        state = {"included": set(), "excluded": set()}
        args.yaml.unlink(missing_ok=True)
        for mp in missions:
            out_dir = args.output_root / mp.name
            if out_dir.is_dir():
                shutil.rmtree(out_dir)
    else:
        state = load_yaml(args.yaml)

    args.output_root.mkdir(parents=True, exist_ok=True)

    all_frames = [(mp, mp.name, f)
                  for mp in missions for f in get_frames(mp)]

    n_inc = sum(1 for _, mn, f in all_frames if fkey(mn, f) in state["included"])
    n_exc = sum(1 for _, mn, f in all_frames if fkey(mn, f) in state["excluded"])

    print(f"\n  Input root     : {args.input_root}")
    print(f"  RGB root       : {args.rgb_root}")
    print(f"  Output root    : {args.output_root}")
    print(f"  YAML           : {args.yaml}")
    print(f"  auto-thresh    : trav_score < {args.auto_thresh:.2f} → removed_nodes")
    print(f"  Param filter   : {args.param or 'all'}")
    print(f"  Param subdirs  : {len(missions)}")
    print(f"  Frames total   : {len(all_frames)}")
    print(f"  Kept / Rejected: {n_inc} / {n_exc}  "
          f"(unlabeled: {len(all_frames) - n_inc - n_exc})")
    print(f"\n  drag/r/c/u=edit nodes  1/2/s=label frame  "
          f"n/b or arrows=nav  j=next-unlabeled  q=quit\n")

    tool = ExplorfmTool(
        all_frames, state, args.yaml,
        input_root  = args.input_root,
        rgb_root    = args.rgb_root,
        output_root = args.output_root,
        auto_thresh = args.auto_thresh,
        skip_labeled = args.skip_labeled,
        no_confirm   = args.no_confirm,
    )
    tool.run()


if __name__ == "__main__":
    main()
