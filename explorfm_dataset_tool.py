#!/usr/bin/env python3
"""
ExplORFM manual cleaning tool.

Mirrors `dataset_tool.py` but operates on the *score-tagged* graphs written by
the ExplORFM auto-cleaner: each `nodes[i]` has a `trav_score` / `frontier_score`
field (see `nebula2-wildos/explorfm/inspect_cleaned_jsons.ipynb`).

  - Reads graph JSONs from --input-root  (default longrange_explorfm_clean)
  - Reads RGB frames     from --rgb-root    (default longrange)
  - Writes manually-cleaned JSONs to --output-root
        (default longrange_explorfm_manually_cleaned)
  - On the first visit to a frame, every node with `trav_score < --auto-thresh`
    is pre-seeded into `removed_nodes`; subsequent visits load from the output
    tree, so your edits persist.
  - Every node is colour-tinted by its `trav_score`
    (red = low confidence, green = high) and frontier nodes use a star marker
    so you can spot ExplORFM's calls at a glance before refining them.

Controls
────────
  Left-drag        Draw a selection rectangle (stack multiple)
  r                Remove highlighted active nodes (red preview)
  a                Restore highlighted removed nodes back to active
                   (cyan preview) — re-attaches any edges whose endpoints
                   are both active again
  c                Clear pending rectangles (no JSON change)
  u                Undo ALL edits on this frame
                   (resets to the trav_score pre-seed)
  h                Toggle visibility of already-removed (gray) nodes
  1                Keep frame (mark included, auto-advance)
  2                Reject frame (mark excluded, auto-advance)
  s                Skip (leave unlabeled, advance)
  n / →            Next frame
  b / ←            Previous frame
  j                Jump forward to next unlabeled frame
  q / ESC          Quit

Usage
─────
  python explorfm_dataset_tool.py
  python explorfm_dataset_tool.py --auto-thresh 0.4
  python explorfm_dataset_tool.py --missions 2024-10-01
  python explorfm_dataset_tool.py --revert-all          # restore output JSONs
                                                        #   (re-applies pre-seed
                                                        #    on next visit)
  python explorfm_dataset_tool.py --fresh               # wipe YAML, restart
"""

