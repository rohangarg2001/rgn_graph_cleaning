#!/usr/bin/env python3
"""
Dataset cleaning tool for ASL RGB NAV GRAPH dataset.

Displays overlay images (with nav-graph nodes) and lets you label each frame.
Labels are saved incrementally to a single YAML file across all missions.

Controls:
  1       - Keep this frame (mark as included)
  2       - Reject this frame (mark as excluded)
  b       - Go back to previous frame
  s       - Skip (leave frame unlabeled for now)
  t       - Toggle between overlay and raw RGB view
  q / ESC - Quit and save

Usage:
  python clean_dataset.py                              # All missions, skip already-labeled
  python clean_dataset.py --missions 2024-10-01        # Partial name match
  python clean_dataset.py --missions FULL_FOLDER_NAME  # Exact mission folder name
  python clean_dataset.py --redo                       # Re-show already-labeled frames
  python clean_dataset.py --fresh                      # Ignore existing YAML, start fresh
  python clean_dataset.py --yaml path/to/custom.yaml   # Custom YAML path
"""

import sys
import yaml
import cv2
import argparse
from pathlib import Path

DATASET_ROOT = Path(__file__).parent
DEFAULT_YAML = DATASET_ROOT / "dataset_cleaning.yaml"

WINDOW_NAME = "Dataset Cleaner"
MAX_W = 1440
MAX_H = 900


# ── YAML helpers ──────────────────────────────────────────────────────────────

def load_state(yaml_path: Path) -> dict:
    if yaml_path.exists():
        with open(yaml_path) as f:
            data = yaml.safe_load(f) or {}
        return {
            "included": set(data.get("included") or []),
            "excluded": set(data.get("excluded") or []),
        }
    return {"included": set(), "excluded": set()}


def save_state(state: dict, yaml_path: Path):
    data = {
        "included": sorted(state["included"]),
        "excluded": sorted(state["excluded"]),
    }
    header = (
        "# Dataset cleaning results - generated by clean_dataset.py\n"
        "# Entry format:  mission_folder/frame_index\n"
        "# Files per frame: rgb_XXXXXX.png  overlay_XXXXXX.png  "
        "overlay_edges_XXXXXX.png  graph_XXXXXX.json\n\n"
    )
    with open(yaml_path, "w") as f:
        f.write(header)
        yaml.dump(data, f, default_flow_style=False, sort_keys=False, allow_unicode=True)


# ── Dataset helpers ───────────────────────────────────────────────────────────

def get_missions(root: Path, names: list | None) -> list[Path]:
    all_missions = sorted(d for d in root.iterdir()
                          if d.is_dir() and d.name.startswith("trial_"))
    if not names:
        return all_missions
    result = []
    for pattern in names:
        matched = [m for m in all_missions if pattern in m.name]
        if matched:
            result.extend(matched)
        else:
            print(f"[warn] no mission matched: {pattern!r}")
    # preserve order, deduplicate
    seen = set()
    return [m for m in result if not (m in seen or seen.add(m))]


def get_frames(mission: Path) -> list[str]:
    return sorted(p.stem.split("_", 1)[1]
                  for p in mission.glob("overlay_[0-9]*.png"))


def fkey(mission_name: str, frame_idx: str) -> str:
    return f"{mission_name}/{frame_idx}"


# ── Display helpers ───────────────────────────────────────────────────────────

def resize_fit(img):
    h, w = img.shape[:2]
    scale = min(MAX_W / w, MAX_H / h, 1.0)
    if scale < 1.0:
        img = cv2.resize(img, (int(w * scale), int(h * scale)),
                         interpolation=cv2.INTER_AREA)
    return img


def draw_hud(img, *, mission_name, frame_idx,
             frame_pos, frame_total,
             mission_pos, mission_total,
             status, labeled_total, dataset_total,
             view_mode):
    h, w = img.shape[:2]
    font = cv2.FONT_HERSHEY_SIMPLEX

    # top bar
    bar = img.copy()
    cv2.rectangle(bar, (0, 0), (w, 110), (0, 0, 0), -1)
    cv2.addWeighted(bar, 0.55, img, 0.45, 0, img)

    # bottom bar
    bar2 = img.copy()
    cv2.rectangle(bar2, (0, h - 38), (w, h), (0, 0, 0), -1)
    cv2.addWeighted(bar2, 0.55, img, 0.45, 0, img)

    # mission line
    cv2.putText(img, f"Mission {mission_pos}/{mission_total}: {mission_name}",
                (10, 26), font, 0.55, (210, 210, 210), 1, cv2.LINE_AA)

    # frame line
    cv2.putText(img,
                f"Frame: {frame_idx}   [{frame_pos}/{frame_total} in mission]   "
                f"dataset labeled: {labeled_total}/{dataset_total}",
                (10, 56), font, 0.55, (200, 200, 200), 1, cv2.LINE_AA)

    # view mode
    view_str = "[overlay+nodes]" if view_mode == "overlay" else "[raw RGB]"
    cv2.putText(img, f"View: {view_str}  (t=toggle)",
                (10, 85), font, 0.5, (150, 200, 255), 1, cv2.LINE_AA)

    # status badge (top-right)
    if status == "KEPT":
        sc = (60, 220, 60)
    elif status == "REJECTED":
        sc = (60, 60, 220)
    else:
        sc = (180, 180, 60)
    (tw, _), _ = cv2.getTextSize(status, font, 0.8, 2)
    cv2.putText(img, status, (w - tw - 14, 40), font, 0.8, sc, 2, cv2.LINE_AA)

    # controls bar
    cv2.putText(img,
                "1=Keep   2=Reject   b=Back   s=Skip   t=Toggle view   q/ESC=Quit",
                (10, h - 10), font, 0.5, (170, 170, 170), 1, cv2.LINE_AA)

    return img


# ── Main cleaning loop ────────────────────────────────────────────────────────

def run_cleaner(missions: list[Path], state: dict, yaml_path: Path, redo: bool):
    # Build the work queue: only frames that need labeling
    queue: list[tuple[Path, str, str]] = []  # (mission_path, mission_name, frame_idx)
    all_frame_counts: dict[str, int] = {}

    for mp in missions:
        frames = get_frames(mp)
        all_frame_counts[mp.name] = len(frames)
        for f in frames:
            k = fkey(mp.name, f)
            if redo or (k not in state["included"] and k not in state["excluded"]):
                queue.append((mp, mp.name, f))

    if not queue:
        print("Nothing to label — all frames already labeled. Use --redo to revisit.")
        return

    # Precompute dataset total (all frames in all chosen missions)
    dataset_total = sum(all_frame_counts.values())

    # Mission ordering for display
    mission_order = {mp.name: i + 1 for i, mp in enumerate(missions)}
    total_missions = len(missions)

    print(f"\n  Frames to label : {len(queue)}")
    print(f"  Dataset total   : {dataset_total}")
    print(f"  YAML            : {yaml_path}")
    print("\n  Controls: 1=Keep  2=Reject  b=Back  s=Skip  t=Toggle  q=Quit\n")

    cv2.namedWindow(WINDOW_NAME, cv2.WINDOW_NORMAL)
    cv2.resizeWindow(WINDOW_NAME, MAX_W, MAX_H)

    # Per-mission frame index cache to show position within the full mission
    mission_all_frames: dict[str, list[str]] = {
        mp.name: get_frames(mp) for mp in missions
    }

    view_mode = "overlay"  # or "rgb"
    i = 0

    while i < len(queue):
        mp, mname, fidx = queue[i]

        # Load image based on current view mode
        def load_img(mode):
            prefix = "overlay" if mode == "overlay" else "rgb"
            path = mp / f"{prefix}_{fidx}.png"
            return cv2.imread(str(path))

        img = load_img(view_mode)
        if img is None:
            print(f"[warn] cannot load image for {mname}/{fidx}, skipping")
            i += 1
            continue

        img = resize_fit(img)

        k = fkey(mname, fidx)
        if k in state["included"]:
            status = "KEPT"
        elif k in state["excluded"]:
            status = "REJECTED"
        else:
            status = "UNLABELED"

        # Frame position within full mission
        all_f = mission_all_frames[mname]
        frame_pos = all_f.index(fidx) + 1 if fidx in all_f else "?"

        labeled_total = len(state["included"]) + len(state["excluded"])

        draw_hud(img,
                 mission_name=mname,
                 frame_idx=fidx,
                 frame_pos=frame_pos,
                 frame_total=all_frame_counts[mname],
                 mission_pos=mission_order.get(mname, "?"),
                 mission_total=total_missions,
                 status=status,
                 labeled_total=labeled_total,
                 dataset_total=dataset_total,
                 view_mode=view_mode)

        cv2.imshow(WINDOW_NAME, img)

        # Input loop for this frame
        while True:
            key = cv2.waitKey(0) & 0xFF

            if key == ord('1'):
                state["included"].discard(k)
                state["excluded"].discard(k)
                state["included"].add(k)
                save_state(state, yaml_path)
                i += 1
                break

            elif key == ord('2'):
                state["included"].discard(k)
                state["excluded"].discard(k)
                state["excluded"].add(k)
                save_state(state, yaml_path)
                i += 1
                break

            elif key == ord('b'):
                if i > 0:
                    i -= 1
                break

            elif key == ord('s'):
                i += 1
                break

            elif key == ord('t'):
                view_mode = "rgb" if view_mode == "overlay" else "overlay"
                new_img = load_img(view_mode)
                if new_img is not None:
                    new_img = resize_fit(new_img)
                    draw_hud(new_img,
                             mission_name=mname, frame_idx=fidx,
                             frame_pos=frame_pos,
                             frame_total=all_frame_counts[mname],
                             mission_pos=mission_order.get(mname, "?"),
                             mission_total=total_missions,
                             status=status,
                             labeled_total=labeled_total,
                             dataset_total=dataset_total,
                             view_mode=view_mode)
                    cv2.imshow(WINDOW_NAME, new_img)
                # stay in inner loop

            elif key in (ord('q'), 27):  # q or ESC
                kept = len(state["included"])
                rejected = len(state["excluded"])
                print(f"\n  Quit.  Kept: {kept}  Rejected: {rejected}  "
                      f"Unlabeled: {dataset_total - kept - rejected}")
                cv2.destroyAllWindows()
                return

    kept = len(state["included"])
    rejected = len(state["excluded"])
    print(f"\n  Done!  Kept: {kept}  Rejected: {rejected}  "
          f"Unlabeled: {dataset_total - kept - rejected}")
    cv2.destroyAllWindows()


# ── Entry point ───────────────────────────────────────────────────────────────

def main():
    parser = argparse.ArgumentParser(
        description="Interactive dataset cleaner for ASL RGB NAV GRAPH.",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=__doc__,
    )
    parser.add_argument(
        "--missions", nargs="+", metavar="NAME",
        help="Mission folder names or partial matches (e.g. 2024-10-01). "
             "Default: all missions.",
    )
    parser.add_argument(
        "--redo", action="store_true",
        help="Re-show already-labeled frames so you can change them.",
    )
    parser.add_argument(
        "--fresh", action="store_true",
        help="Ignore existing YAML labels and start from scratch.",
    )
    parser.add_argument(
        "--yaml", type=Path, default=DEFAULT_YAML, metavar="PATH",
        help=f"YAML file path. Default: dataset_cleaning.yaml next to this script.",
    )
    args = parser.parse_args()

    state = {} if args.fresh else load_state(args.yaml)
    if args.fresh:
        state = {"included": set(), "excluded": set()}

    missions = get_missions(DATASET_ROOT, args.missions)
    if not missions:
        print("[error] No missions found.")
        sys.exit(1)

    print(f"\n  Dataset root : {DATASET_ROOT}")
    print(f"  YAML file    : {args.yaml}")
    print(f"  Missions     : {len(missions)}")
    for mp in missions:
        frames = get_frames(mp)
        n_labeled = sum(
            1 for f in frames
            if fkey(mp.name, f) in state.get("included", set())
            or fkey(mp.name, f) in state.get("excluded", set())
        )
        print(f"    {mp.name}  ({n_labeled}/{len(frames)} labeled)")

    run_cleaner(missions, state, args.yaml, args.redo)


if __name__ == "__main__":
    main()
