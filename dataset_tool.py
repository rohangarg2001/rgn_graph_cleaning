#!/usr/bin/env python3
"""
Unified dataset tool: frame cleaning + node editing in one window.

No modes — every action is always available on every frame.

Controls
────────
  Left-drag        Draw a selection rectangle (stack multiple)
  r                Confirm: remove highlighted nodes + their edges
  c                Clear pending rectangles (no JSON change)
  u                Undo ALL node removals on this frame (restore JSON)
  1                Keep frame (mark included, auto-advance)
  2                Reject frame (mark excluded, auto-advance)
  s                Skip (leave unlabeled, advance)
  n  / →           Next frame
  b  / ←           Previous frame
  j                Jump forward to next unlabeled frame
  q  / ESC         Quit

Usage
─────
  python dataset_tool.py                        # all missions, resume
  python dataset_tool.py --mission-root DIR     # use any parent directory;
                                                #   every subfolder containing
                                                #   overlay_*.png is a mission
  python dataset_tool.py --missions 2024-10-01  # partial name match
  python dataset_tool.py --fresh                # wipe YAML, restart clean
  python dataset_tool.py --skip-labeled         # jump to first unlabeled
  python dataset_tool.py --yaml path/to/f.yaml  # custom YAML path
  python dataset_tool.py --revert-all           # restore every graph JSON
                                                #   (move removed_* back into
                                                #    nodes/edges) and exit
"""

import json
import sys
import argparse
from pathlib import Path

import cv2
import numpy as np
import yaml

# ── Config ────────────────────────────────────────────────────────────────────

DATASET_ROOT = Path(__file__).parent.parent
DEFAULT_YAML = DATASET_ROOT / "rgn_graph_cleaning/longrange_cleaning.yaml"
# DEFAULT_YAML = "/home/rohang73/Documents/asl_rgb_stuff/rgn_graph_cleaning/longrange_cleaning.yaml"
WINDOW       = "Dataset Tool"
MAX_W, MAX_H = 1440, 900

# BGR colours
C_ACTIVE   = (  0, 255,   0)   # green  – normal node (max-saturation for visibility)
C_SELECTED = ( 40,  40, 230)   # red    – pending removal
C_REMOVED  = (130, 130, 130)   # gray   – removed manually (removed_nodes)
C_OCCLUDED = (255, 255, 255)   # white  – removed by occlusion_clean.py
C_RECT     = (230, 140,  20)   # amber  – selection rectangle


# ── I/O helpers ───────────────────────────────────────────────────────────────

def load_yaml(path: Path) -> dict:
    if path.exists():
        d = yaml.safe_load(path.read_text()) or {}
        return {"included": set(d.get("included") or []),
                "excluded": set(d.get("excluded") or [])}
    return {"included": set(), "excluded": set()}


def save_yaml(state: dict, path: Path):
    header = (
        "# Dataset cleaning results - generated by dataset_tool.py\n"
        "# Entry format:  mission_folder/frame_index\n"
        "# Files per frame: rgb_XXXXXX.png  overlay_XXXXXX.png  "
        "overlay_edges_XXXXXX.png  graph_XXXXXX.json\n\n"
    )
    data = {"included": sorted(state["included"]),
            "excluded": sorted(state["excluded"])}
    with open(path, "w") as f:
        f.write(header)
        yaml.dump(data, f, default_flow_style=False, sort_keys=False)


def load_json(path: Path) -> dict:
    return json.loads(path.read_text()) if path.exists() else {}


def save_json(path: Path, data: dict):
    path.write_text(json.dumps(data, indent=2))


# ── Dataset helpers ───────────────────────────────────────────────────────────

def get_missions(mission_root: Path, names: list | None) -> list[Path]:
    # A "mission" is any subdirectory of `mission_root` that contains at least
    # one overlay_*.png frame. This handles both trial_* and other layouts
    # (e.g. longrange/highrange_*) without hardcoding a naming convention.
    all_m = sorted(d for d in mission_root.iterdir()
                   if d.is_dir() and any(d.glob("overlay_[0-9]*.png")))
    if not names:
        return all_m
    result, seen = [], set()
    for pat in names:
        for m in all_m:
            if pat in m.name and m not in seen:
                result.append(m)
                seen.add(m)
    if not result:
        print(f"[warn] no missions matched: {names}")
    return result


def get_frames(mp: Path) -> list[str]:
    return sorted(p.stem.split("_", 1)[1]
                  for p in mp.glob("overlay_[0-9]*.png"))


def fkey(mname: str, fidx: str) -> str:
    return f"{mname}/{fidx}"


# ── Geometry helpers ──────────────────────────────────────────────────────────

def fit_scale(shape) -> float:
    h, w = shape[:2]
    return min(MAX_W / w, MAX_H / h, 1.0)


def to_disp(px, py, s) -> tuple[int, int]:
    return int(px * s), int(py * s)


def norm_rect(a, b) -> tuple:
    return (min(a[0], b[0]), min(a[1], b[1]),
            max(a[0], b[0]), max(a[1], b[1]))


def pt_in_rects(x, y, rects) -> bool:
    return any(x1 <= x <= x2 and y1 <= y <= y2 for x1, y1, x2, y2 in rects)


# ── Main tool ─────────────────────────────────────────────────────────────────

class DatasetTool:

    def __init__(self, all_frames, state, yaml_path, skip_labeled,
                 no_confirm=False):
        self.all_frames = all_frames
        self.total      = len(all_frames)
        self.state      = state
        self.yaml_path  = yaml_path
        self.no_confirm = no_confirm

        # Per-frame node-edit state
        self.rects:  list  = []
        self.drawing       = False
        self.pt0           = None
        self.pt1           = None

        # Current-frame cache (populated by _load)
        self._orig:  np.ndarray | None = None
        self._scale: float             = 1.0
        self._graph: dict              = {}
        self._gpath: Path | None       = None

        self.idx = 0
        if skip_labeled:
            self.idx = self._find_unlabeled(start=0) or 0

    # ── Unlabeled navigation ──────────────────────────────────────────────────

    def _is_unlabeled(self, i: int) -> bool:
        _, mn, f = self.all_frames[i]
        k = fkey(mn, f)
        return k not in self.state["included"] and k not in self.state["excluded"]

    def _find_unlabeled(self, start: int) -> int | None:
        for i in range(start, self.total):
            if self._is_unlabeled(i):
                return i
        return None

    # ── Mouse callback ────────────────────────────────────────────────────────

    def mouse_cb(self, event, x, y, _flags, _p):
        if event == cv2.EVENT_LBUTTONDOWN:
            self.drawing = True
            self.pt0 = self.pt1 = (x, y)
        elif event == cv2.EVENT_MOUSEMOVE and self.drawing:
            self.pt1 = (x, y)
        elif event == cv2.EVENT_LBUTTONUP and self.drawing:
            self.drawing = False
            self.pt1 = (x, y)
            r = norm_rect(self.pt0, self.pt1)
            if (r[2] - r[0]) > 4 and (r[3] - r[1]) > 4:
                self.rects.append(r)
                if self.no_confirm:
                    self.apply_removal()      # immediate, no `r` press needed
            self.pt0 = self.pt1 = None

    # ── Graph edit operations ─────────────────────────────────────────────────

    def _selected_ids(self) -> set:
        if not self.rects or not self._graph:
            return set()
        s = self._scale
        return {n["id"] for n in self._graph.get("nodes", [])
                if pt_in_rects(*to_disp(*n["pixel"], s), self.rects)}

    def apply_removal(self):
        sel = self._selected_ids()
        if not sel:
            return
        g = self._graph
        g.setdefault("removed_nodes", [])
        g.setdefault("removed_edges", [])

        keep_n = [n for n in g["nodes"] if n["id"] not in sel]
        rem_n  = [n for n in g["nodes"] if n["id"] in sel]
        keep_e = [e for e in g["edges"]
                  if e["node_id_0"] not in sel and e["node_id_1"] not in sel]
        rem_e  = [e for e in g["edges"]
                  if e["node_id_0"] in sel or e["node_id_1"] in sel]

        g["nodes"] = keep_n
        g["removed_nodes"].extend(rem_n)
        g["edges"] = keep_e
        g["removed_edges"].extend(rem_e)
        save_json(self._gpath, g)
        self.rects = []
        print(f"  [{self._gpath.name}] -{len(rem_n)} nodes  -{len(rem_e)} edges  "
              f"(active: {len(keep_n)}  total removed: {len(g['removed_nodes'])})")

    def undo_edit(self):
        g = self._graph
        rn = g.pop("removed_nodes", [])
        re = g.pop("removed_edges", [])
        if not rn and not re:
            return
        g.setdefault("nodes", []).extend(rn)
        g.setdefault("edges", []).extend(re)
        save_json(self._gpath, g)
        print(f"  [{self._gpath.name}] restored {len(rn)} nodes / {len(re)} edges")

    # ── Clean label operations ─────────────────────────────────────────────────

    def _label(self, bucket: str):
        _, mn, f = self.all_frames[self.idx]
        k = fkey(mn, f)
        other = "excluded" if bucket == "included" else "included"
        self.state[other].discard(k)
        self.state[bucket].discard(k)
        self.state[bucket].add(k)
        save_yaml(self.state, self.yaml_path)
        self._advance()

    def _advance(self):
        if self.idx < self.total - 1:
            self.idx += 1
            self._load()

    # ── Rendering ─────────────────────────────────────────────────────────────

    def _render(self) -> np.ndarray:
        s   = self._scale
        img = self._orig.copy()
        if s < 1.0:
            h, w = self._orig.shape[:2]
            img = cv2.resize(img, (int(w * s), int(h * s)),
                             interpolation=cv2.INTER_AREA)

        g   = self._graph
        sel = self._selected_ids()

        # Active nodes — drawn fully opaque with a black outer ring so the
        # green pops on any background.
        for n in g.get("nodes", []):
            dp  = to_disp(*n.get("pixel", (0, 0)), s)
            col = C_SELECTED if n["id"] in sel else C_ACTIVE
            cv2.circle(img, dp, 4, (0, 0, 0), -1, cv2.LINE_AA)   # halo
            cv2.circle(img, dp, 3, col,       -1, cv2.LINE_AA)

        # Confirmed rectangles (edit mode)
        for (x1, y1, x2, y2) in self.rects:
            ov = img.copy()
            cv2.rectangle(ov,  (x1, y1), (x2, y2), C_RECT, -1)
            cv2.addWeighted(ov, 0.13, img, 0.87, 0, img)
            cv2.rectangle(img, (x1, y1), (x2, y2), C_RECT, 2)

        # Live drag rectangle
        if self.drawing and self.pt0 and self.pt1:
            cv2.rectangle(img, self.pt0, self.pt1, (180, 220, 255), 1)

        self._draw_hud(img, sel)
        return img

    def _draw_hud(self, img, sel: set):
        h, w  = img.shape[:2]
        fn    = cv2.FONT_HERSHEY_SIMPLEX
        g     = self._graph
        _, mn, fidx = self.all_frames[self.idx]

        # ── semi-transparent bars ──
        for (y0, y1) in [(0, 122), (h - 42, h)]:
            ov = img.copy()
            cv2.rectangle(ov, (0, y0), (w, y1), (0, 0, 0), -1)
            cv2.addWeighted(ov, 0.55, img, 0.45, 0, img)

        # ── clean status badge (top-right) ──
        k = fkey(mn, fidx)
        if   k in self.state["included"]:  s_txt, s_col = "KEPT",     (55, 210, 55)
        elif k in self.state["excluded"]:  s_txt, s_col = "REJECTED", (55,  55, 210)
        else:                               s_txt, s_col = "UNLABELED",(160, 160, 55)
        (tw, _), _ = cv2.getTextSize(s_txt, fn, 0.70, 2)
        cv2.putText(img, s_txt, (w - tw - 14, 32), fn, 0.70, s_col, 2, cv2.LINE_AA)

        # ── info lines ──
        cv2.putText(img, f"Mission: {mn}",
                    (10, 26), fn, 0.50, (210, 210, 210), 1, cv2.LINE_AA)

        n_inc = len(self.state["included"])
        n_exc = len(self.state["excluded"])
        cv2.putText(img,
                    f"Frame: {fidx}  [{self.idx+1}/{self.total}]   "
                    f"kept: {n_inc}  rejected: {n_exc}  unlabeled: {self.total - n_inc - n_exc}",
                    (10, 52), fn, 0.49, (200, 200, 200), 1, cv2.LINE_AA)

        # ── node stats ──
        na, nr, no, ns = (len(g.get("nodes", [])),
                          len(g.get("removed_nodes", [])),
                          len(g.get("occlusion_cleaned_nodes", [])),
                          len(sel))
        stats = f"Nodes active: {na}   removed: {nr}   occluded: {no}"
        if ns: stats += f"   pending removal: {ns}"
        cv2.putText(img, stats, (10, 80), fn, 0.49,
                    (60, 110, 230) if ns else (160, 215, 160), 1, cv2.LINE_AA)

        # ── controls footer ──
        ctrl = ("drag=select  r=remove  c=clear  u=undo  "
                "1=keep  2=reject  s=skip  j=next-unlabeled  "
                "n/b=nav  q=quit")
        cv2.putText(img, ctrl, (10, h - 13), fn, 0.42,
                    (165, 165, 165), 1, cv2.LINE_AA)

    # ── Main event loop ───────────────────────────────────────────────────────

    def run(self):
        if not self.all_frames:
            print("No frames found.")
            return

        cv2.namedWindow(WINDOW, cv2.WINDOW_NORMAL)
        cv2.resizeWindow(WINDOW, MAX_W, MAX_H)
        cv2.setMouseCallback(WINDOW, self.mouse_cb)
        self._load()

        while True:
            cv2.imshow(WINDOW, self._render())
            key = cv2.waitKey(30) & 0xFF

            if key in (ord('n'), 83):               # n / right-arrow
                if self.idx < self.total - 1:
                    self.idx += 1
                    self.rects = []
                    self._load()

            elif key in (ord('b'), 81):             # b / left-arrow
                if self.idx > 0:
                    self.idx -= 1
                    self.rects = []
                    self._load()

            elif key == ord('j'):                   # jump to next unlabeled
                nxt = self._find_unlabeled(self.idx + 1)
                if nxt is not None:
                    self.idx = nxt
                    self.rects = []
                    self._load()

            elif key in (ord('q'), 27):             # quit
                break

            elif key == ord('1'):                   # keep
                self._label("included")

            elif key == ord('2'):                   # reject
                self._label("excluded")

            elif key == ord('s'):                   # skip
                self._advance()

            elif key == ord('r'):                   # confirm node removal
                self.apply_removal()

            elif key == ord('c'):                   # clear pending rectangles
                self.rects = []

            elif key == ord('u'):                   # undo all removals
                self.undo_edit()

        cv2.destroyAllWindows()
        ni = len(self.state["included"])
        ne = len(self.state["excluded"])
        print(f"\n  Quit — kept: {ni}  rejected: {ne}  "
              f"unlabeled: {self.total - ni - ne}")

    # ── Frame loader ──────────────────────────────────────────────────────────

    def _load(self):
        mp, _mn, fidx = self.all_frames[self.idx]
        img_path = mp / f"rgb_{fidx}.png"
        gpath    = mp / f"graph_{fidx}.json"

        img = cv2.imread(str(img_path))
        if img is None:
            print(f"[warn] cannot load {img_path}")
            img = np.zeros((720, 1280, 3), dtype=np.uint8)

        self._orig  = img
        self._scale = fit_scale(img.shape)
        self._graph = load_json(gpath)
        self._gpath = gpath

        self.rects = []
        self.drawing = False
        self.pt0 = self.pt1 = None


# ── Bulk revert ───────────────────────────────────────────────────────────────

def revert_all(missions: list[Path]):
    """Move removed_nodes/removed_edges back into nodes/edges for every
    graph_*.json under the given missions. Idempotent; safe to re-run."""
    print(f"\n  Reverting graph JSONs in {len(missions)} mission(s)…")
    total_files = restored_files = restored_n = restored_e = 0
    for mp in missions:
        for gp in sorted(mp.glob("graph_[0-9]*.json")):
            total_files += 1
            g = load_json(gp)
            rn = g.pop("removed_nodes", []) or []
            re_ = g.pop("removed_edges", []) or []
            if not rn and not re_:
                continue
            g.setdefault("nodes", []).extend(rn)
            g.setdefault("edges", []).extend(re_)
            save_json(gp, g)
            restored_files += 1
            restored_n     += len(rn)
            restored_e     += len(re_)
    print(f"  Scanned {total_files} files; restored {restored_files} "
          f"({restored_n} nodes, {restored_e} edges).\n")


# ── Entry point ───────────────────────────────────────────────────────────────

def main():
    ap = argparse.ArgumentParser(
        description="Unified dataset cleaning + node editing tool.",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=__doc__,
    )
    ap.add_argument("--mission-root", type=Path, default=DATASET_ROOT,
                    metavar="DIR",
                    help="Parent directory containing mission subfolders. "
                         "Any subdirectory holding overlay_*.png is treated as "
                         f"a mission. Default: {DATASET_ROOT}")
    ap.add_argument("--missions",     nargs="+", metavar="NAME",
                    help="Mission folder names or partial matches under "
                         "--mission-root. Default: all.")
    ap.add_argument("--fresh",        action="store_true",
                    help="Wipe existing YAML and restart cleaning from scratch.")
    ap.add_argument("--skip-labeled", action="store_true",
                    help="Auto-jump to the first unlabeled frame on startup.")
    ap.add_argument("--yaml",         type=Path, default=DEFAULT_YAML,
                    metavar="PATH",   help="YAML file path.")
    ap.add_argument("--revert-all",   action="store_true",
                    help="Restore every graph_*.json: move removed_nodes/edges "
                         "back into nodes/edges, then exit. Honours --missions.")
    ap.add_argument("--no-confirm",   action="store_true",
                    help="Apply node removal immediately on mouse-up. Each "
                         "rectangle drag commits straight to the graph JSON "
                         "with no `r` press. Use with care — `c` becomes "
                         "useless and `u` is your only undo.")
    args = ap.parse_args()

    if not args.mission_root.is_dir():
        print(f"[error] --mission-root not a directory: {args.mission_root}")
        sys.exit(1)

    missions = get_missions(args.mission_root, args.missions)
    if not missions:
        print("[error] No missions found.")
        sys.exit(1)

    if args.revert_all:
        revert_all(missions)
        return

    if args.fresh:
        state = {"included": set(), "excluded": set()}
        args.yaml.unlink(missing_ok=True)
    else:
        state = load_yaml(args.yaml)

    all_frames = [(mp, mp.name, f)
                  for mp in missions for f in get_frames(mp)]

    n_inc = sum(1 for _, mn, f in all_frames if fkey(mn, f) in state["included"])
    n_exc = sum(1 for _, mn, f in all_frames if fkey(mn, f) in state["excluded"])

    print(f"\n  Mission root   : {args.mission_root}")
    print(f"  YAML           : {args.yaml}")
    print(f"  Missions       : {len(missions)}")
    print(f"  Frames total   : {len(all_frames)}")
    print(f"  Kept / Rejected: {n_inc} / {n_exc}  "
          f"(unlabeled: {len(all_frames) - n_inc - n_exc})")
    print(f"\n  drag/r/c/u=edit nodes  1/2/s=label frame  "
          f"n/b or arrows=nav  j=next-unlabeled  q=quit\n")

    tool = DatasetTool(
        all_frames, state, args.yaml,
        skip_labeled = args.skip_labeled,
        no_confirm   = args.no_confirm,
    )
    tool.run()


if __name__ == "__main__":
    main()
